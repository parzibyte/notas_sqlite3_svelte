<script>
    import { createEventDispatcher } from "svelte";
    import { Icon, LockClosed } from "svelte-hero-icons";
    import { fade, slide } from "svelte/transition";
    export let titulo = "Introducir dato",
        mensajeConfirmar = "SÃ­",
        mensajeCancelar = "Cancelar",
        error = "",
        mostrar = false;
    let valor = "";
    let input = null;
    $: if (mostrar) {
        setTimeout(() => {
            input.focus();
        }, 10);
    }
    const dispatch = createEventDispatcher();

    const cancelar = () => {
        dispatch("cancelar");
    };

    const confirmar = () => {
        if (!valor) {
            return;
        }
        dispatch("confirmar", valor);
    };
</script>

<div
    transition:fade={{ duration: 300 }}
    class="relative z-10"
    class:invisible={!mostrar}
    class:visible={mostrar}
    aria-labelledby="modal-title"
    role="dialog"
    aria-modal="true"
>
    <div class="fixed inset-0 bg-gray-500 bg-opacity-75 transition-opacity" />

    <div class="fixed inset-0 z-10 overflow-y-auto">
        <div
            class="flex min-h-full items-end justify-center p-4 text-center sm:items-center sm:p-0"
        >
            <div
                class="relative transform overflow-hidden rounded-lg bg-white text-left shadow-xl transition-all sm:my-8 sm:w-full sm:max-w-lg"
            >
                <div class="bg-white px-4 pb-4 pt-5 sm:p-6 sm:pb-4">
                    <div class="sm:flex sm:items-start">
                        <div
                            class="mx-auto flex h-12 w-12 flex-shrink-0 items-center justify-center rounded-full bg-red-100 sm:mx-0 sm:h-10 sm:w-10"
                        >
                            <Icon
                                src={LockClosed}
                                outline
                                class="h-8 w-8 text-pink-500"
                            />
                        </div>
                        <div
                            class="w-full mt-3 text-center sm:ml-4 sm:mt-0 sm:text-left"
                        >
                            <h3
                                class="text-base font-semibold leading-6 text-gray-900"
                                id="modal-title"
                            >
                                {titulo}
                            </h3>
                            <div class="mt-2">
                                <input
                                    bind:this={input}
                                    type="password"
                                    on:keydown={(evento) => {
                                        if (evento.key === "Enter") {
                                            confirmar();
                                        }
                                    }}
                                    bind:value={valor}
                                    class="bg-gray-50 border border-gray-300 text-gray-900 text-sm rounded-lg focus:ring-blue-500 focus:border-blue-500 block w-full p-2.5"
                                />
                            </div>
                            {#if error}
                                <div transition:slide>
                                    <strong class="text-red-700">{error}</strong
                                    >
                                </div>
                            {/if}
                        </div>
                    </div>
                </div>
                <div
                    class="bg-gray-50 px-4 py-3 sm:flex sm:flex-row-reverse sm:px-6"
                >
                    <button
                        on:click={confirmar}
                        type="button"
                        class="inline-flex w-full justify-center rounded-md bg-pink-600 px-3 py-2 text-sm font-semibold text-white shadow-sm hover:bg-pink-500 sm:ml-3 sm:w-auto"
                        >{mensajeConfirmar}</button
                    >
                    <button
                        on:click={cancelar}
                        type="button"
                        class="mt-3 inline-flex w-full justify-center rounded-md bg-white px-3 py-2 text-sm font-semibold text-gray-900 shadow-sm ring-1 ring-inset ring-gray-300 hover:bg-gray-50 sm:mt-0 sm:w-auto"
                        >{mensajeCancelar}</button
                    >
                </div>
            </div>
        </div>
    </div>
</div>
